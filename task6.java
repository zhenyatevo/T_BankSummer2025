/*В одной известной it-компании решили провести мероприятие для укрепления коллектива. Было проведено несколько игр на знакомства и узнавание интересов.
В одной из игр всех сотрудников расставили в ряд, у каждого сотрудника известен рост, и у -го работника он равен . В некоторые моменты игры два человека, которые стоят рядом, знакомились и выходили из ряда. После этого левая и правая половина ряда сдвигались, чтобы не было пустоты между ними. Такое общение происходило до тех пор, пока в ряду есть хотя бы два сотрудника.
Авторы игры решили, что чем больше разница между людьми, которые знакомятся, тем веселее. Поэтому они захотели найти такой способ знакомства, чтобы суммарная разница роста людей, которые знакомятся, была как можно больше.
Так как они не программисты, а всего лишь авторы игр, то не знают, как найти лучший порядок. Помогите им решить эту задачу.
Формат входных данных
В первой строке входных данных дано число  количество сотрудников
Во второй строке дан массив целых чисел
Формат выходных данных
Выведите наибольшую суммарную разницу в росте сотрудников, которые познакомятся.

Замечание
В первом примере могут познакомиться сотрудники с ростом 2 и 4, затем сотрудники с ростом 1 и 3, тогда суммарная разница будет |2 - 4| + |1 - 3| = 4.
ввод
4
1 2 4 3
вывод
4
ввод
5
2 1 4 5 2
вывод
6*/
import java.util.ArrayList;
import java.util.Scanner;

public class task6 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int n = scanner.nextInt();
        ArrayList<Integer> heights = new ArrayList<>();
        for (int i = 0; i < n; i++) {
            heights.add(scanner.nextInt());
        }

        int totalDifference = 0;

        while (heights.size() >= 2) {
            int maxDiff = 0;
            int index = 0;

            // Находим пару соседей с максимальной разницей
            for (int i = 0; i < heights.size() - 1; i++) {
                int diff = Math.abs(heights.get(i) - heights.get(i + 1));
                if (diff > maxDiff) {
                    maxDiff = diff;
                    index = i;
                }
            }

            totalDifference += maxDiff;
            // Удаляем пару с максимальной разницей
            heights.remove(index);
            heights.remove(index); // После удаления первого элемента, второй сдвигается на его место
        }

        System.out.println(totalDifference);
    }
}
