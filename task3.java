/*Петя играл на чердаке и нашел в сундуке запылившийся массив целых чисел. Пете показалось, что в этом массиве слишком много одинаковых элементов, и его нужно как-то разнообразить. Петя еще не ходит в школу, поэтому он умеет только делить числа на 2 с округлением вниз. Теперь его интересует, сколько различных элементов он может сделать, если он может сколько угодно раз проделывать изменения. После применения операции элемент заменяется на полученное число.
Формат входных данных
В первой строке дано целое число  длина массива
Во второй строке дан массив целых чисел  длины
Формат выходных данных
Выведите, какое максимальное число различных чисел можно получить.

Замечание
Во втором примере можно получить массив 0, 1, 1, 2, 2.
вход
3
4 4 2
вывод
3
вход
5
1 1 1 2 2
вывод
3*/
import java.util.*;

public class task3 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int n = scanner.nextInt();
        Integer[] arr = new Integer[n];
        for (int i = 0; i < n; i++) {
            arr[i] = scanner.nextInt();
        }

        // Сортируем в порядке убывания, чтобы обрабатывать большие числа первыми
        Arrays.sort(arr, Collections.reverseOrder());

        Set<Integer> unique = new HashSet<>();
        for (int num : arr) {
            while (num > 0) {
                if (!unique.contains(num)) {
                    unique.add(num);
                    break;
                }
                num /= 2;
            }
            // Если после всех делений num = 0, добавляем 0, если его еще нет
            if (num == 0 && !unique.contains(0)) {
                unique.add(0);
            }
        }

        System.out.println(unique.size());
    }
}
